# Problem Description:

# Reverse Polish Notation (RPN) is a different way to write math expressions. For example:

# Standard: 10 + 2
# RPN: 10 2 +
# Your job is to get an input in RPN format and calculate the result.

# Example

# Input: ["2", "1", "+", "3", "*"]
# Output: 9 (Explanation: ((2 + 1) * 3) = 9)