# Remove Duplicates from Sorted Array (Easy)**
# Problem**: Given a sorted array `nums`, remove the duplicates in-place such that each element appears only once and returns the new length. Do not allocate extra space for another array; you must do this by modifying the input array with O(1) extra memory.
# Example**: Input: `nums = [1,1,2]`, After your function runs, `nums` should be modified to `[1,2]`.

